// Declare array sales 
let sales = [120, 85, 200, 150, 90];

// Use for loop to calculate total sales
forloopTotal = 0;
for (let i = 0; i < sales.length; i++) {
    forloopTotal += sales[i];
}

// Log total sales
console.log(`Total sales: $${forloopTotal}`);